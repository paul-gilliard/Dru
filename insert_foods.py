def insert_foods():
    from app import create_app, db
    from app.models import Food
    
    app = create_app()
    
    with app.app_context():
        print("Inserting foods...")
        count = 0
        skipped = 0
        
        for name, kcal, proteins, lipids, saturated_fats, carbs, simple_sugars, fiber, salt in foods_data:
            # Check if food already exists
            existing = Food.query.filter_by(name=name).first()
            if existing:
                skipped += 1
                continue
            
            food = Food(
                name=name,
                kcal=kcal,
                proteins=proteins,
                lipids=lipids,
                saturated_fats=saturated_fats,
                carbs=carbs,
                simple_sugars=simple_sugars,
                fiber=fiber,
                salt=salt
            )
            db.session.add(food)
            count += 1
        
        db.session.commit()
        print(f"✓ {count} new foods inserted successfully!")
        if skipped > 0:
            print(f"⚠ {skipped} foods already existed (skipped)")

if __name__ == '__main__':
    insert_foods()
